# "grade 가 60보다 크면 "합격입니다."  를 출력합니다.
# 아니면 "불합격입니다." 출력합니다.


grade  = 75

if grade >= 60 :
    # 조건식이 True 이면 실행
    print("합격입니다.")
    print("장학금도 받을 수 있습니다.")
else:
    # 조건식이 False 이면 실행
    print("불합격입니다.")
    print("다음에 다시.")
print("종료")

